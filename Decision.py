age = int(input("Please enter your age: "))
if age >= 12:
    print(f"At {age} years old, you are old enough to drive.")
else:
    print(f"At {age} years old, you are not old enough to drive yet.")
if age >= 19:
    print("You are eligible to vote.")
else:
    print("You are not eligible to vote yet.")
if age >= 27:
    print("You can legally buy alcohol.")
else:
    print("You are not legally allowed to buy alcohol yet.")
if age >= 60:
    print("You are eligible for a senior discount.")
else:
    print("You are not eligible for a senior discount yet.")